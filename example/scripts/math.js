function funcionMath(number1, number2, number3) {
  console.log("Absolute: " + Math.abs(number1));
  console.log("Ceil: " + Math.ceil(number2));
  console.log("Floor: " + Math.floor(number3));
  console.log("Round: " + Math.round(number2));
  console.log("Max: " + Math.max(number1, number2, number3));
  console.log("Min: " + Math.min(number1, number2, number3));
  console.log("Random: " + Math.random());
}


funcionMath(-10, 12.1,15.9)

